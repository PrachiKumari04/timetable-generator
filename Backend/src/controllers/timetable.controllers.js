import { Timetable } from "../models/timetable.models.js";
import { ApiError } from "../utils/ApiError.js";
import { ApiResponse } from "../utils/ApiResponse.js";
import { asyncHandler } from "../utils/asyncHandler.js";
import { paginateMongoose, parsePaginationParams } from "../utils/pagination.js";

//* Add timetables
export const addTimetables = asyncHandler(async (req, res) => {
  const timetables = req.body;

  if (!Array.isArray(timetables) || timetables.length === 0) {
    throw new ApiError(400, "Timetable data is required and must be an array");
  }

  //* Validate each timetable
  timetables.forEach((tt) => {
    if (!tt.timetable_id) throw new ApiError(400, "Timetable ID is required");
    if (!tt.semester_id) throw new ApiError(400, "Semester ID is required");
    if (!tt.academicYear) throw new ApiError(400, "Academic Year is required");
    if (!tt.generatedBy) throw new ApiError(400, "Generated By is required");
  });

  //* Filter out existing timetables
  const ttIds = timetables.map((t) => t.timetable_id);
  const existingTimetables = await Timetable.find({
    timetable_id: { $in: ttIds }
  });
  const existingTtIds = new Set(existingTimetables.map((t) => t.timetable_id));

  const uniqueTimetables = timetables.filter(
    (t) => !existingTtIds.has(t.timetable_id)
  );

  if (uniqueTimetables.length === 0) {
    throw new ApiError(408, "All provided timetables already exist");
  }

  const ttRecords = await Timetable.insertMany(uniqueTimetables);

  return res
    .status(201)
    .json(new ApiResponse(201, ttRecords, "Timetables added successfully"));
});

//! Get all timetables with pagination
export const getAllTimetables = asyncHandler(async (req, res) => {
  const { page, limit } = parsePaginationParams(req.query);
  const { search, sortBy, sortOrder, ...fieldFilters } = req.query;

  //! Build filter
  let filter = {};
  
  //* Search filter
  if (search) {
    filter.$or = [
      { timetable_id: { $regex: search, $options: "i" } },
      { semester_id: { $regex: search, $options: "i" } },
      { academicYear: { $regex: search, $options: "i" } },
      { generatedBy: { $regex: search, $options: "i" } },
    ];
  }
  
  // Field-specific filters
  Object.keys(fieldFilters).forEach(key => {
    if (key.startsWith('filter_') && fieldFilters[key] !== undefined && fieldFilters[key] !== '') {
      const fieldName = key.replace('filter_', '');
      const value = fieldFilters[key];
      
      //! Handle boolean filters
      if (value === 'true' || value === 'false') {
        filter[fieldName] = value === 'true';
      } else {
        filter[fieldName] = value;
      }
    }
  });

  //! Build sort
  let sort = {};
  if (sortBy) {
    sort[sortBy] = sortOrder === "desc" ? -1 : 1;
  }

  const result = await paginateMongoose(Timetable, filter, page, limit, { sort });

  return res
    .status(200)
    .json(new ApiResponse(200, result, "Timetables retrieved successfully"));
});

//! Get timetable by ID
export const getTimetableById = asyncHandler(async (req, res) => {
  const { id } = req.params;

  if (!id) throw new ApiError(400, "Timetable ID is required");

  const timetable = await Timetable.findById(id);

  if (!timetable) {
    throw new ApiError(404, "Timetable not found");
  }

  return res
    .status(200)
    .json(new ApiResponse(200, timetable, "Timetable fetched successfully"));
});

//* Update timetable
export const updateTimetable = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const updateData = req.body;

  if (!id) throw new ApiError(400, "Timetable ID is required");

  if (!updateData || Object.keys(updateData).length === 0) {
    throw new ApiError(400, "Update data is required");
  }

  const updatedTimetable = await Timetable.findByIdAndUpdate(
    id,
    updateData,
    { new: true }
  );

  if (!updatedTimetable) {
    throw new ApiError(404, "Timetable not found");
  }

  return res
    .status(200)
    .json(new ApiResponse(200, updatedTimetable, "Timetable updated successfully"));
});

//! Delete timetable
export const deleteTimetable = asyncHandler(async (req, res) => {
  const { id } = req.params;

  if (!id) throw new ApiError(400, "Timetable ID is required");

  const deletedTimetable = await Timetable.findByIdAndDelete(id);

  if (!deletedTimetable) throw new ApiError(404, "Timetable not found");

  return res
    .status(200)
    .json(new ApiResponse(200, deletedTimetable, "Timetable deleted successfully"));
});
